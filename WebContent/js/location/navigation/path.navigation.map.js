function initiMap() {
	speed = 0;
	heading = 0;

	var styledMap = new google.maps.StyledMapType(styles, {
		name : "Styled Map"
	});

	map = new google.maps.Map(document.getElementById('map_canvas'), {
		center : {
			lat : -34.009211,
			lng : 25.669051
		},
		zoom : 14,
		zoomControl : false,
		streetViewControl : false,
		mapTypeControl : false,
		rotateControl : false,
		fullscreenControl : false,
		labels : true
	// ,
	});
	map.mapTypes.set('map_style', styledMap);
	map.setMapTypeId('map_style');
	input = document.getElementById('to');
	map.controls[google.maps.ControlPosition.RIGHT_TOP].push(document
			.getElementById('searchLeftSideBTN'));
	map.controls[google.maps.ControlPosition.LEFT_TOP].push(document
			.getElementById('menuLeftSideBTN'));
	map.controls[google.maps.ControlPosition.LEFT_TOP].push(document
			.getElementById('menuItems'));
	map.setMapTypeId('map_style');
	// findMyLocation();
	$("#mapViewIcon").fadeOut();
	selectMapMode();
	mapSattelView();
	getAllLocations();
}

function zoomInMap() {
	var zoom = map.getZoom();
	zoom++;
	map.setZoom(zoom);
}

function zoomOutMap() {
	var zoom = map.getZoom();
	zoom--;
	map.setZoom(zoom);
}

